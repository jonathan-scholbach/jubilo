<h1>Gegenstand</h1>
<span class="brand">Nun singet und seid froh</span> ist eine Edition, die Advents-, Weihnachts- und Winterlieder zugänglich machen will. Die vier wichtigsten Merkmale der Edition sind. Unsere Vorstellung und Motivation ist, den Zugang zu dieser Literatur zu verbessern. Verschiedene Barrieren schränken die Zugänglichkeit ein - entsprechend reagiert unser Projekt darauf:
<ul>
  <li><b>Kosten</b>: Die Kosten, Noten zu beschaffen, können für einen Chor ein Hinderungsgrund sein, ein Stück zu singen. Daher sind alle Noten hier gemeinfrei und können kostenlos genutzt werden.</li>
  <li><b>Kenntnis</b>: Die grundlegende Voraussetzung, um ein Stück der Chorliteratur zu pflegen, ist die Kenntnis des Stücks. Indem wir auch unbekanntere Stücke herausgeben, wollen wir dazu anregen, auch Stücke zu singen, die sonst in Bibliotheken und Archiven zu verstauben drohen. Dazu dient auch der Katalog mit seinen an der Chorpraxis orientierten Filtern, mit deren Hilfe man sich schnell einen Überblick über auch unbekannte Stücke machen kann, die für den eigenen Chor in Frage kommen könnten.
  </li>
  <li><b>Verständnis</b>: Viele Weihnachtslieder sind alt. Daher sind die Texte oft unverständlich oder fremd. Mit Kommentaren, die diese Unverständlichkeiten erläutern und auflösen, wollen wir die Texte für Zeitgenoss*innen aufschließen. Auch Fremdsprachigkeit ist eine Hürde des Verständnisses. Daher streben wir für fremdsprachige Texte Übersetzungen und Lautschrift an.</li>
  <li><b>Schwierigkeit</b>: Nicht zuletzt kann ein Stück für einen bestimmten Chor zu schwer sein. Um auch Laienchören zu ermöglichen, schwierigere Stücke zu erarbeiten, bieten wir für jedes Stück MIDI-Dateien mit hervorgehobenen Einzelstimmen, um das individuelle Üben auch ohne Instrument zu erleichtern.</li>
</ul> 

