<h1>Gegenstand</h1>
<span class="brand">Nun singet und seid froh</span> ist eine Edition, die Advents-, Weihnachts- und Winterlieder zugänglich machen will. Die vier wichtigsten Merkmale der Edition sind:
    <ul>
    <li><b>Freiwilligkeit</b>: <span class="brand">Nun singet und seid froh</span> wird von Freiwilligen erarbeitet.</li>
    <li><b>Work in progress</b>: Alles hier ist im Fluss. Kaum etwas ist vollständig. Wir haben eine Vision - eine umfassende Sammlung von Weihnachtsliedern - und der Umstand, dass das Ziel in weiter Ferne liegt, hält uns nicht davon ab, mit der Arbeit anzufangen und auch schon Teiletappen hier zu veröffentlichen.</li>
    <li><b>Barrierefreiheit</b>: Verschiedene Barrieren schränken die Zugänglichkeit ein - entsprechend reagiert unser Projekt darauf:
        <ul>
        <li><b>Kosten</b>: Die Kosten, Noten zu beschaffen, können für einen Chor ein Hinderungsgrund sein, ein Stück zu singen. Daher sind alle Noten hier gemeinfrei und können kostenlos genutzt werden.</li>
        <li><b>Kenntnis</b>: Die grundlegende Voraussetzung, um ein Stück der Chorliteratur zu pflegen, ist die Kenntnis des Stücks. Indem wir auch unbekanntere Stücke herausgeben, wollen wir dazu anregen, auch Stücke zu singen, die sonst in Bibliotheken und Archiven zu verstauben drohen. Dazu dient auch der filterbare Katalog, mithilfe dessen man sich schnell einen Überblick über auch unbekannte Stücke machen kann, die für den eigenen Chor in Frage kommen könnten.
        </li>
        <li><b>Verständnis</b>: Viele Weihnachtslieder sind alt. Daher sind die Texte oft unverständlich oder fremd. Mit wissenschaftlichen Kommentaren wollen wir diese fremden Texte für Zeitgenoss*innen aufschließen. Auch Fremdsprachigkeit ist eine Hürde des Verständnisses. Daher bieten wir für fremdsprachige Texte Übersetzungen und Lautschrift.</li>
        <li><b>Schwierigkeit</b>: Nicht zuletzt kann ein Stück für einen bestimmten Chor zu schwer sein. Um auch weniger professionellen Chören zu ermöglichen, schwierigere Stücke zu erarbeiten, bieten wir für jedes Stück MIDI-Dateien mit hervorgehobenen Einzelstimmen, um das individuelle Üben auch ohne Instrument zu erleichtern.</li>
        </ul>
       </li>
       <li><b>Wissenschaftlichkeit</b>: Wir geben die Quellen unserer Notenausgabe an, wir suchen nach guten Quellen und vergleichen verschiedene Quellen, um zu einem Resultat zu gelangen, das wissenschaftlich vertretbar ist.</li>
       </ul> 
